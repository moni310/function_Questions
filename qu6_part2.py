def calculater(n1,n2,operation):
    if operation =="+":
        num1=n1+n2
        return (num1)
    elif operation =="-":
        num2=n1-n2
        return(num2)
    elif operation =="*":
        num3=n1*n2
        return(num3)
    elif operation =="%":
        num4=n1%n2
        return(num4)
    elif operation =="//":
        num5=n1//n2
        return(num5)
